          <p>
          Citumang Pangandaran merupakan salah satu destinasi wisata dari seluruh destinasi yang ada di Pangandaran. Dewasa ini objek wisata citumang tidak hanya dikunjungi oleh wisatawan lokal tetapi juga telah banyak di kunjungi wisatawan dari mancanegara sehingga kini namanya menjadi Green Valley Citumang agar lebih mendunia. 
          <br>
          Citumang sendiri merupakan wisata alam dimana disana wisatawan bisa menemukan sungai dengan air yang bening kebiruan dikelilingi pepohonan yang rindang, udara yang sejuk, suara satwa hutan yang tak pernah sepi dan suasana yang alami terasa tepat jika dijadikan tempat untuk melepas lelah, kepenatan dan kehidupan sibuknya suasana perkotaan. Tidak hanya itu Citumang juga menawarkan wisata air body rafting dimana wisatawan berenang menyusuri beningnya air sungai menggunakan rompi pelampung dengan durasi 3-4 jam. 
          <br>
          Objek wisata ini terletak di Desa Bojong Kecamatan Parigi Pangandaran, berjarak 19 km dari Pangandaran ke arah barat. Dapat ditempuh dengan kendaraan pribadi kurang lebih 40 menit.
